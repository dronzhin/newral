while True:
    year = input("Введите год рождения Пушкина: ")
    if year == "1799":
        print("Год указан верно")
        break
    else:
        print("Год рождения Пушкина указан не верно")
while True:
    day = input("Введите день рождения Пушкина: ")
    if day == "6":
        print("Верно, Пушкин родился 6 июня 1799 года")
        break
    else:
        print("День рождения Пушкина указан не верно")